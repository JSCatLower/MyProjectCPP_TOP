#include "../../include/math.hpp"
#include <cmath>

double add(double a, double b) {
    return a + b;
}
double sub(double a, double b) {
    return a - b;
}
double mult(double a, double b) {
    return a * b;
}
double divide(double a, double b) {
    return a / b;
}
double square(double x) {
    return x * x;
}
double power(double a, double b) {
    return pow(a, b);
}
